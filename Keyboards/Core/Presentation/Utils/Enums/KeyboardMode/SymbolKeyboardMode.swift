enum SymbolKeyboardMode {
    case `default`
    case URL
    case emailAddress
    case webSearch
    
    var keyList: [[[[String]]]] {
        // TODO: iOS 26에서 기호 키보드 하나로 통합?
        switch self {
        case .default, .webSearch:
            return [
                [
                    [ ["1"], ["2"], ["3"], ["4"], ["5"], ["6"], ["7"], ["8"], ["9"], ["0"] ],
                    [ ["-"], ["/"], [":"], [";"], ["("], [")"], ["₩"], ["&"], ["@"], ["\""] ],
                    [ ["."], [","], ["?"], ["!"], ["'"] ]
                ],
                [
                    [ ["["], ["]"], ["{"], ["}"], ["#"], ["%"], ["^"], ["*"], ["+"], ["="] ],
                    [ ["_"], ["\\"], ["|"], ["~"], ["<"], [">"], ["$"], ["£"], ["¥"], ["•"] ],
                    [ ["."], [","], ["?"], ["!"], ["'"] ]
                ]
            ]
        case .URL:
            return [
                [
                    [ ["1"], ["2"], ["3"], ["4"], ["5"], ["6"], ["7"], ["8"], ["9"], ["0"] ],
                    [ ["@"], ["&"], ["%"], ["?"], [","], ["="], ["["], ["]"], [], [] ],
                    [ ["_"], [":"], ["-"], ["+"], [] ]
                ],
                [
                    [ ["1"], ["2"], ["3"], ["4"], ["5"], ["6"], ["7"], ["8"], ["9"], ["0"] ],
                    [ ["*"], ["$"], ["#"], ["!"], ["'"], ["^"], ["["], ["]"], [], [] ],
                    [ ["~"], [";"], ["("], [")"], [] ]
                ]
            ]
        case .emailAddress:
            return [
                [
                    [ ["1"], ["2"], ["3"], ["4"], ["5"], ["6"], ["7"], ["8"], ["9"], ["0"] ],
                    [ ["$"], ["!"], ["~"], ["&"], ["="], ["#"], ["["], ["]"], [], [] ],
                    [ ["."], ["_"], ["-"], ["+"], [] ]
                ],
                [
                    [ ["'"], ["|"], ["{"], ["}"], ["?"], ["%"], ["^"], ["*"], ["/"], ["’"] ],
                    [ ["$"], ["!"], ["~"], ["&"], ["="], ["#"], ["["], ["]"], [], [] ],
                    [ ["."], ["_"], ["-"], ["+"], [] ]
                ]
            ]
        }
    }
}
